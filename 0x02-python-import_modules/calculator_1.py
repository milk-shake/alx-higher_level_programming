#!/usr/bin/python3
def add(a, b):
    result = a + b 
    print (f"{a} + {b} = {result}")
def sub(a, b):
    result = a - b 
    print (f"{a} - {b} = {result}")
def mul(a, b):
    result = a * b 
    print (f"{a} * {b} = {result}")
def div(a, b):
    result = a // b 
    print (f"{a} / {b} = {result}")
